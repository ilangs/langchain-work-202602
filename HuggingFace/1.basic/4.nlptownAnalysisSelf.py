'''
nlptown/bert-base-multilingual-uncased-sentiment 모델은 '아마존,옐프 상품리뷰 기반 학습 모델'이어서
뉴스/금융 텍스트에는 취약하고, 다국어 모델로서 한국어 임베딩 표현력이 현저히 떨어집니다.

1. 학습 데이터 도메인의 불일치 (Domain Mismatch)
사용하신 nlptown/bert-base-multilingual-uncased-sentiment 모델은 '상품 리뷰(Product Reviews)' 데이터(아마존, 옐프 등)를 기반으로 1~5점 별점을 예측하도록 파인튜닝(Fine-tuning)된 모델입니다.

- 리뷰어투 텍스트에는 강함: "최고입니다!"(0.84), "강력히 추천"(0.55), "화가 납니다"(0.46) 같은 전형적인 상품/서비스 리뷰 어휘가 포함된 문장은 그나마 잘 맞춥니다.
- 뉴스/금융 텍스트에는 취약: 2번 문장("실적이 예상치를 상회하며...")이나 3번 문장("인터페이스 직관적...")과 같은 비즈니스·IT 뉴스 형태의 문장은 모델이 학습한 '리뷰'의 패턴과 달라 맥락을 전혀 파악하지 못하고 낮은 신뢰도(0.29, 0.43)로 엉뚱한 클래스(1 star, 2 stars)를 내뱉게 됩니다.

2. 다국어(Multilingual) 모델의 한국어 이해도 한계
이 모델은 100개가 넘는 언어를 동시에 처리하는 Multilingual BERT 기반입니다. 한국어 전용 코퍼스로 학습된 모델이 아니기 때문에, 한국어 고유의 형태소 변화나 어휘(특히 경제/비즈니스 용어)에 대한 임베딩(Embedding) 표현력이 한국어 전용 모델(KoELECTRA, KR-FinBert 등)에 비해 현저히 떨어집니다.

💡 해결 방안: 도메인 특화 한국어 전용 모델 사용
현재 개발 중이신 '한국어 금융/일반 뉴스 감정 분석' 파이프라인에 맞게, 모델을 금융/경제 뉴스 도메인에 특화된 snunlp/KR-FinBert-SC (또는 일반 한국어 감정분석 모델)로 교체하셔야 정확한 결과를 얻을 수 있습니다.

'''
