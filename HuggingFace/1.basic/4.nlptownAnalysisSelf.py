from transformers import pipeline

# --- [1. 감정 분석 파이프라인 생성] ---
# 'nlptown/bert-base-multilingual-uncased-sentiment' 모델은
# 1~5점 척도로 분류하며, 긍정 문장은 4~5 stars, 부정 문장은 1~2 stars로 결과 산출
classifier = pipeline(
    "sentiment-analysis",  
    model="nlptown/bert-base-multilingual-uncased-sentiment"
)

# --- [2. 분석할 한국어 문장 배열(List) 생성] ---
# 긍정과 부정의 상황(일상, 비즈니스, 리뷰 등)을 혼합하여 10개 구성
news_texts = [
    # 긍정 문장 (5개)
    "오늘은 날씨가 맑고 따뜻해서 산책하기에 정말 좋은 하루입니다.",
    "이번 분기 실적이 예상치를 크게 웃돌며 시장에 긍정적인 활력을 불어넣고 있습니다.",
    "새롭게 출시된 서비스는 사용자 인터페이스가 직관적이고 반응 속도가 매우 빠릅니다.",
    "고객 지원팀의 빠르고 친절한 응대 덕분에 문제가 완벽하게 해결되었습니다. 최고입니다!",
    "제품의 마감 처리가 훌륭하고 내구성도 뛰어나서 주변에 강력히 추천하고 싶네요.",
    
    # 부정 문장 (5개)
    "주문한 지 한 달이 지났는데 아직도 배송 지연 안내조차 없어서 정말 화가 납니다.",
    "소프트웨어 업데이트 이후 앱이 계속 튕기고 버그가 너무 많아 정상적인 사용이 불가능하네요.",
    "배송된 제품의 포장 상태가 엉망이고 스크래치까지 다수 발견되어 크게 실망했습니다.",
    "원자재 가격 급등과 금리 인상 여파로 중소기업들의 연쇄 도산 위험이 커지고 있습니다.",
    "상담원의 불친절한 태도와 무책임한 일 처리 방식 때문에 다시는 이용하고 싶지 않습니다."
]

# --- [3. 감정 분석 실행 및 결과 출력] ---
print("=" * 50)
print("다국어 감정 분석 결과 (1~5 Stars)")
print("=" * 50)

# pipeline에 리스트를 직접 전달하면 배치 처리가 되어 더 효율적입니다.
results = classifier(news_texts)

# --- [4. 결과 출력 포맷팅] ---
# zip 함수를 사용하여 원본 텍스트와 분석 결과를 매핑하여 출력합니다.
for idx, (text, result) in enumerate(zip(news_texts, results), 1):
    label = result['label']
    score = round(result['score'], 4)
    
    print(f"[{idx:02d}] 문장: {text}")
    print(f"     ▷ 감성: {label},  ▷ 신뢰도: {score}") 
    print("-" * 50)

