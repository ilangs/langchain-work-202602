# --- [필수 라이브러리 임포트] ---
from transformers import pipeline

# --- [1. 감정 분석 파이프라인 생성] ---
classifier = pipeline(
    "sentiment-analysis",
    model="nlptown/bert-base-multilingual-uncased-sentiment",
    framework="pt"
)

# --- [2. 분석할 한국어 문장 배열 (긍정 + 부정)] ---
news_texts = [
    # 긍정 문장
    "오늘은 날씨가 맑고 따뜻해서 기분이 매우 좋습니다.",
    "이번 프로젝트는 매우 성공적으로 마무리되었습니다.",
    "서비스 품질이 뛰어나서 고객 만족도가 높습니다.",
    "경제가 점차 회복되고 있어 긍정적인 전망이 보입니다.",
    "이 제품은 기대 이상으로 훌륭합니다.",

    # 부정 문장
    "오늘 주식시장은 큰 폭으로 하락하여 투자자들이 불안해하고 있습니다.",
    "서비스 장애로 인해 많은 사용자가 불편을 겪었습니다.",
    "이번 결과는 매우 실망스럽습니다.",
    "경제 상황이 악화되고 있어 우려가 커지고 있습니다.",
    "이 제품은 품질이 좋지 않아 추천하기 어렵습니다."
]

# --- [3. 감정 분석 실행 및 결과 출력] ---
for idx, text in enumerate(news_texts, 1):
    result = classifier(text)[0]

    print(f"\n[{idx}] 문장: {text}")
    print("감정 분석 결과:", result['label'])   # 예: '1 star' ~ '5 stars'
    print("확률 점수:", round(result['score'], 4))
    
