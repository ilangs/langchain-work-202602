from langchain_openai import ChatOpenAI
from langchain_core.prompts import ChatPromptTemplate
from langchain_core.output_parsers import StrOutputParser

import dotenv
dotenv.load_dotenv()

model = ChatOpenAI(model="gpt-4o-mini",temperature=0.1) 
parser = StrOutputParser()

prompt1 = ChatPromptTemplate.from_template("{item}을 활용한 혁신적인 미디어 컨텐츠 아이디어를 하나 제안해 줘.")
prompt2 = ChatPromptTemplate.from_template("다음 아이디어의 예상되는 기술적 문제점 2가지를 알려줘: {idea}")

chain1 = prompt1 | model | parser
chain2 = prompt2 | model | parser

idea = chain1.invoke({"item": "홀로그램"})
print(f"아이디어: {idea}\n")

problems = chain2.invoke({"idea": idea})
print(f"예상 문제점: \n{problems}")


